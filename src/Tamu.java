// Tamu.java
public class Tamu {
    private String nama;
    private String nomorKontak;

    public Tamu(String nama, String nomorKontak) {
        this.nama = nama;
        this.nomorKontak = nomorKontak;
    }

    public String getNama() {
        return nama;
    }

    public String getNomorKontak() {
        return nomorKontak;
    }
}
